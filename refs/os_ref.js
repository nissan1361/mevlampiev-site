const os = require('os');

// Platform
console.log(os.platform());

// Architecture
console.log(os.arch())

// Processor
console.log(os.cpus())

// Free RAM
console.log(os.freemem())

// All RAM
console.log(os.totalmem())

// Root dir
console.log(os.homedir())

// System work time
console.log(os.uptime())